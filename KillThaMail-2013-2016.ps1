	$buttonTestIt_Click = {
		if (($textbox_To.Text -eq "" -or $textbox_To.Text -eq $null) -and ($textbox_From.Text -eq "" -or $textbox_From.Text -eq $null) -and ($textbox_Subject.Text -eq "" -or $textbox_Subject.Text -eq $null) -and ($datetimepicker_Start.Value -eq "01/01/2000 00:00:00"))
		{
			$textbox_Status.Text = "Please enter some search criteria....`r`nYou really don't want me to return your entire database....`r`nYou're gonna have a bad time..."
		}
		if (($textbox_To.Text -eq "" -or $textbox_To.Text -eq $null) -and ($textbox_From.Text -eq "" -or $textbox_From.Text -eq $null) -and ($datetimepicker_Start.Value -eq "01/01/2000 00:00:00") -and ($textbox_Subject.Text -ne "" -and $textbox_Subject.Text -ne $null))
		{
			$textbox_Status.Text = "When searching by subject, please include a From or To address. Searches using only Subject will not return the results you expect."
		}
		else
		{
			$textbox_Status.Text = "Running search:`r`n"
			$global:TestStatus = $true
			$global:LogDir = $textbox_LogDir.Text
			$TempDir = Split-Path -Path $global:LogDir
			if ((Test-Path $TempDir) -eq $false) { New-Item -ItemType Directory -Path $TempDir }
			$UserDateTime = $datetimepicker_Start.Value -match "(\d\d\/\d\d\/\d\d\d\d)"
			$UserDateTime = $Matches[($Matches.Count - 1)]
			if ($UserDateTime -eq "01/01/2000") { $UserDateTime = $null }
			$global:SearchQuery = Get-MailboxSearchQuery -To $textbox_To.Text -From $textbox_From.Text -Subject $textbox_Subject.Text -DateSent $UserDateTime
			$textbox_Status.Text += "$global:SearchQuery`r`n`r`nThis may take some time..."
			$MailboxSearch = Get-MailboxSearchTest -SearchQuery $global:SearchQuery
			
			foreach ($Object in $MailboxSearch)
			{
				if ($Object.ResultItemsCount -ne 0) { $global:DiscoveredObjects += $Object }
			}
			$TotalResults = $global:DiscoveredObjects.count
			$textbox_Status.Text = "Total objects found: $TotalResults`r`n`r`n"
			$textbox_Status.Text += "Objects found for:`r`n"
			$textbox_Status.Text += $DiscoveredObjects.Identity
			$global:DiscoveredObjects | Export-Csv -Path $global:LogDir -NoTypeInformation
			Get-NewLog
		}
	}
	
	$buttonKillIt_Click = {
		if ($global:TestStatus -eq $true -and $global:TotalResults -ne 0)
		{
			$textbox_Status.Text = "Removing mail objects...`r`n"
			
			if ($global:CopyToAdmin -eq $false)
			{
				
				foreach ($Object in $global:DiscoveredObjects)
				{
					$ObjectIdentity = $Object.Identity
					$textbox_Status.Text += "Removing Object from: $ObjectIdentity"
					Get-Mailbox -Identity $ObjectIdentity | Search-Mailbox -SearchQuery $global:SearchQuery -DeleteContent -Force
				}
			}
			if ($global:CopyToAdmin -eq $true)
			{
				
				foreach ($Object in $global:DiscoveredObjects)
				{
					$ObjectIdentity = $Object.Identity
					$textbox_Status.Text += "Copying Object to Search Results Folder in Administrator mailbox`r`nRemoving Object from:`r`n$ObjectIdentity"
					Get-Mailbox -Identity $ObjectIdentity | Search-Mailbox -TargetMailbox "Administrator" -TargetFolder "Search Results" -SearchQuery $global:SearchQuery -DeleteContent -LogLevel Full -Force
				}
			}
			$global:DiscoveredObjects = @()
		}
		if ($global:TotalResults -eq 0)
		{
			$textbox_Status.Text = "There is nothing to delete, please refine your query."
		}
		if ($global:TestStatus -eq $false)
		{
			$textbox_Status.Text = "Please test your query first!!"
		}
	}
